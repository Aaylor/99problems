/* Problem 001 */
object Problem001 {

  def last[A](l : List[A]) : A = l match {
    case Nil       => throw new NoSuchElementException
    case x :: Nil  => x
    case _ :: tail => last(tail)
  }

}


/* Problem 002 */
object Problem002 {

  def lastButOne[A](l : List[A]) : A = l match {
    case x :: _ :: Nil => x
    case _ :: tail     => lastButOne(tail)
    case _             => throw new NoSuchElementException
  }

}


